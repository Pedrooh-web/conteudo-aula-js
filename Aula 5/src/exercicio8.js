// Exercício 5: Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a área do Triângulo.

alert("Área do Triângulo");

let base = parseFloat(prompt("Informe o valor da base do Triângulo: "));

let altura = parseFloat(prompt("Informe o valor da base do Triângulo: "));


let resultado = (base * altura) /2;

alert("O valor da área do quadrado é: " + resultado);